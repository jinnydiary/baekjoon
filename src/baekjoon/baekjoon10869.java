package baekjoon;

import java.util.Scanner;

public class baekjoon10869 {

	public static void main(String[] args) {
		Scanner scr = new Scanner(System.in);
		int a = scr.nextInt();
		int b = scr.nextInt();
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.print(a%b);
		scr.close();
	}

}
